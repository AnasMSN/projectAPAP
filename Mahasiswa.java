
public class Mahasiswa {
	int npm;
	String name;
	
	public Mahasiswa(){
		int npm;
		String name;
	}

	public int getNpm() {
		return npm;
	}

	public void setNpm(int npm) {
		this.npm = npm;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
	
	
}
